def interactive_login(timeout_seconds: int = LOGIN_TIMEOUT_SECONDS) -> AuthStatus:
    """
    Open Ctrip login page and wait for user to complete login.

    Detects login by polling for the `cticket` cookie every second.
    On success, exports all cookies to Netscape format for httpx reuse.
    """
    console.print("[bold cyan]打开携程登录页，请在浏览器中完成登录…[/bold cyan]")
    console.print(f"[dim]支持：扫码 / 手机验证码 / 账号密码（超时 {timeout_seconds}s）[/dim]")

    try:
        with _open_page(headless=False) as page:
            page.goto(LOGIN_URL, wait_until="domcontentloaded", timeout=30000)
            page.wait_for_timeout(1500)

            start = time.time()
            while time.time() - start < timeout_seconds:
                if _has_cticket(page):
                    # Give page a moment to settle and set all cookies
                    page.wait_for_timeout(2000)
                    _save_netscape_cookies(page)
                    return AuthStatus(
                        status=LoginStatus.LOGGED_IN,
                        message="登录成功，cookies 已保存",
                        checked_at=datetime.now(),
                    )
                page.wait_for_timeout(1000)

            return AuthStatus(
                status=LoginStatus.LOGGED_OUT,
                message=f"等待超时（{timeout_seconds}s），请重试",
                checked_at=datetime.now(),
            )

    except PlaywrightTimeout:
        return AuthStatus(
            status=LoginStatus.UNKNOWN,
            message="打开登录页超时，请检查网络连接",
            checked_at=datetime.now(),
        )
    except Exception as e:
        msg = str(e)
        if "Executable doesn't exist" in msg:
            msg = "Patchright 浏览器未安装，请运行：patchright install chromium"
        elif "Target page" in msg and "closed" in msg:
            msg = "浏览器意外关闭，请重试"
        return AuthStatus(
            status=LoginStatus.UNKNOWN,
            message=msg,
            checked_at=datetime.now(),
        )


def clear_session() -> bool:
    """Delete saved cookies file."""
    path = get_cookies_path()
    if path.exists():
        path.unlink()
        return True
    return False
