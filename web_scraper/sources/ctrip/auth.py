"""Authentication for Ctrip — Playwright interactive login + Netscape cookie export."""
import time
from contextlib import contextmanager
from dataclasses import dataclass
from datetime import datetime
from enum import Enum
from typing import Iterator, Optional

from patchright.sync_api import Page, TimeoutError as PlaywrightTimeout, sync_playwright
from rich.console import Console

from .config import SOURCE_NAME
from .cookies import get_cookies_path

console = Console()

LOGIN_URL = "https://passport.ctrip.com/user/login"
HOME_URL = "https://www.ctrip.com/"
LOGIN_TIMEOUT_SECONDS = 180  # 3 minutes for user to complete login


class LoginStatus(Enum):
    LOGGED_IN = "logged_in"
    LOGGED_OUT = "logged_out"
    UNKNOWN = "unknown"


@dataclass
class AuthStatus:
    status: LoginStatus
    message: str
    checked_at: Optional[datetime] = None


# ─────────────────────────────────────────
# Browser helpers
# ─────────────────────────────────────────

@contextmanager
def _open_page(*, headless: bool = False) -> Iterator[Page]:
    """Launch a Chrome browser and yield a Page, then close cleanly."""
    with sync_playwright() as pw:
        for launch_kwargs in [
            {"channel": "chrome", "headless": headless,
             "args": ["--disable-blink-features=AutomationControlled"]},
            {"headless": headless,
             "args": ["--disable-blink-features=AutomationControlled"]},
        ]:
            browser = None
            try:
                browser = pw.chromium.launch(**launch_kwargs)
                context = browser.new_context(
                    viewport={"width": 1440, "height": 900},
                    locale="zh-CN",
                )
                context.add_init_script(
                    "Object.defineProperty(navigator, 'webdriver', { get: () => undefined });"
                )
                page = context.new_page()
                try:
                    yield page
                finally:
                    try:
                        context.close()
                    finally:
                        browser.close()
                return
            except Exception:
                if browser:
                    try:
                        browser.close()
                    except Exception:
                        pass


def _has_cticket(page: Page) -> bool:
    """Return True if browser context has a valid cticket cookie."""
    try:
        cookies = page.context.cookies()
        for c in cookies:
            if c["name"] == "cticket" and c.get("value"):
                return True
    except Exception:
        pass
    return False


def _save_netscape_cookies(page: Page) -> None:
    """Export all browser cookies to Netscape format file."""
    dest = get_cookies_path()
    dest.parent.mkdir(parents=True, exist_ok=True)

    cookies = page.context.cookies()
    lines = [
        "# Netscape HTTP Cookie File",
        "# Generated by WebScraper Ctrip login",
        "",
    ]
    for c in cookies:
        domain = c.get("domain", "")
        include_subdomains = "TRUE" if domain.startswith(".") else "FALSE"
        path = c.get("path", "/")
        secure = "TRUE" if c.get("secure", False) else "FALSE"
        expires = int(c.get("expires", 0))
        if expires < 0:
            expires = 0
        name = c.get("name", "")
        value = c.get("value", "")
        lines.append(f"{domain}\t{include_subdomains}\t{path}\t{secure}\t{expires}\t{name}\t{value}")

    dest.write_text("\n".join(lines), encoding="utf-8")
    console.print(f"[dim]Cookies 已保存至：{dest}[/dim]")


